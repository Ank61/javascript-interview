var canPlaceFlowers = function (flowerbed, n) {
  let flowers = n;

  for (let i = 0; i < flowerbed.length; i++) {
    if (
      flowerbed[i] === 0 &&
      (i === 0 || flowerbed[i - 1] === 0) &&
      (i === flowerbed.length - 1 || flowerbed[i + 1] === 0)
    ) {
      flowerbed[i] = 1;
      flowers--;

      if (flowers === 0) {
        return true;
      }
    }
  }
  return flowers <= 0;
};
